# Criar um programa que mostre o antecessor e o sucessor de um número inteiro

# Solicita o número ao usuário e armazena em uma variável
numero = int(input("Digite um número: "))

# Calcula o antecessor e o sucessor do número digitado e Imprime o resultado
print(f"O antecessor de {numero} é {numero-1} e o seu sucessor é {numero+1}")
