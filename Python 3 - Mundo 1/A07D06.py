# Desafio 06 - Mostrar o dobro, triplo e raíz quadrada de um número

numero = float(input("Digite um número para calcular o dobro, triplo e raíz quadrada: "))

# Calcula e imprime os resultados
print(f"\nO número digitado foi {numero}\n")
print(f"Dobro: {numero*2}")
print(f"Triplo: {numero*3}")
print(f"Raíz quadrada: {numero**(1/2)}")
