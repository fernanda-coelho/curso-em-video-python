# Desafio 03 - Criar um Programa que mostre a soma entre dois números

# Solicita dois números ao usuário e converte para float
n1 = float(input("Digite o primeiro número: "))
n2 = float(input("Digite o segundo número: "))

# Realiza a soma entre os números e armazena o resultado em uma variável
soma = n1 + n2

# Imprime o resultado da soma
print(f"A soma entre {n1} e {n2} é igual a {soma}")
