# Desafio 02 - Criar um programa que informe a data de nascimento do usuário formatada

# Solicita os dados do nascimento do usuário e armazena em variáveis
dia = input("Digite o dia do seu aniversário: ")
mes = input("Digite o mês que você nasceu: ")
